// 获取所有可用车辆列表
export function getVehiclesList() {
    const allVehicles = {};
    Object.values(vehicles).forEach(category => {
        Object.assign(allVehicles, category);
    });
    return allVehicles;
}

// 检查车辆是否有效
export function isValidVehicle(vehicleName) {
    const allVehicles = getVehiclesList();
    return vehicleName in allVehicles;
}

